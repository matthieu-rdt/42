int		ft_sqrt(int nb)
{
	int n;

	n = 1;
	if (nb <= 0)
	{
		return (0);
	}
	while (n * n < nb && n <= 46340)
	{
		n++;
	}
	if (nb == n * n)
	{
		return (n);
	}
	return (0);
}
