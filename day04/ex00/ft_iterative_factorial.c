int		ft_iterative_factorial(int nb)
{
	int i;

	i = nb;
	if (nb < 0 || nb > 12)
	{
		return (0);
	}
	if (nb == 0 || nb == 1)
	{
		return (1);
	}
	while (i >= 2)
	{
		nb = nb * (i - 1);
		i = i - 1;
	}
	return (nb);
}
